<div class="wrap prose-pc">
  <h1 class="text-2xl font-bold mb-1">Privacy policy</h1>
  <p class="text-muted text-sm mb-6">Last updated 26 June 2026</p>

  <p class="mb-4">
    potcount is a tool for tracking friendly home poker games. This page explains, in plain
    language, what information it keeps about you and why. We try to collect as little as possible.
  </p>

  <h2 class="text-lg font-bold mt-6 mb-2">What we store</h2>
  <ul class="list-disc pl-5 space-y-1 mb-4">
    <li><b>Your account:</b> your name/handle, and — if you sign in with Google or Apple — the ID and profile photo they share with us. You can connect more than one sign-in method (a passcode plus Google and/or Apple) so you don't lose access; you can disconnect the extra ones any time.</li>
    <li><b>Email (optional):</b> if you sign in with Google/Apple we receive your email; with a passcode account it's optional. We store it to identify your account and, <b>only if you tick the box</b>, to send occasional updates.</li>
    <li><b>Optional answers:</b> if you choose to, an age range, where you're from, and how you heard about us. You can skip these.</li>
    <li><b>Your games:</b> the buy-ins, results and settlements you enter, and reactions/comments you post.</li>
    <li><b>A login cookie:</b> a single signed cookie that keeps you logged in. No third-party tracking or advertising cookies.</li>
  </ul>

  <h2 class="text-lg font-bold mt-6 mb-2">Why we use it</h2>
  <ul class="list-disc pl-5 space-y-1 mb-4">
    <li>To run the app, keep you signed in, and show your stats and history.</li>
    <li>To contact you about your account if needed.</li>
    <li>To send a newsletter <b>only</b> if you explicitly opted in — and you can opt out at any time.</li>
    <li>The optional answers help us understand who uses potcount. They're never required.</li>
  </ul>

  <h2 class="text-lg font-bold mt-6 mb-2">Where it's kept</h2>
  <p class="mb-4">
    Your data lives on our own server hosted in the EU (Frankfurt), with encrypted daily backups
    stored via Cloudflare R2. We do <b>not</b> sell your data or share it with advertisers. Sign-in
    is handled by Google and Apple under their own privacy policies; we only receive the basic
    profile details described above.
  </p>

  <h2 class="text-lg font-bold mt-6 mb-2">Your choices and rights</h2>
  <ul class="list-disc pl-5 space-y-1 mb-4">
    <li><b>Newsletter:</b> opt in or out whenever you like.</li>
    <li><b>Email:</b> add, change or remove the optional email on your account at any time.</li>
    <li><b>Access &amp; correction:</b> you can edit your profile yourself, or ask us for a copy of your data.</li>
    <li><b>Deletion:</b> you can delete your account yourself — see below — or ask us to do it for you.</li>
  </ul>

  <h2 class="text-lg font-bold mt-6 mb-2">Deleting your account</h2>
  <p class="mb-4">
    On your <a href="/account">account page</a> there's a <b>Delete account</b> option. To prevent
    accidents it asks you to type your username (and your passcode, if you use one) to confirm. When
    you delete:
  </p>
  <ul class="list-disc pl-5 space-y-1 mb-4">
    <li>Your account record — name/handle, email, passcode, any linked Google/Apple sign-ins, optional answers and follows — is <b>erased</b>, and your username is freed up.</li>
    <li>Comments and reactions you posted are removed.</li>
    <li>Games you played in <b>stay</b>, because they're shared with the other players — but your seat is <b>unlinked</b> from you: it keeps the buy-ins and result under the name on the table, with no link back to a profile. Nobody can reach your (now deleted) profile from those games.</li>
  </ul>
  <p class="mb-4">
    Deletion is immediate and can't be undone. For a short period afterwards a copy may still exist
    in our encrypted daily backups; those rotate automatically and any deleted data is gone from them
    within about two weeks.
  </p>
  <p class="mb-4">
    For any of the above, or any privacy question, email
    <a href="mailto:info@fatcloud.nl">info@fatcloud.nl</a>.
  </p>

  <p class="text-muted text-sm mt-8">
    We may update this policy as potcount grows; the date at the top reflects the latest version.
  </p>
