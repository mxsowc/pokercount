  <h1 class="text-2xl font-bold mb-1">Privacy policy</h1>
  <p class="text-muted text-sm mb-6">Last updated 7 July 2026</p>

  <p class="mb-4">
    potcount is a tool for tracking friendly home poker games. This page explains, in plain
    language, what information it keeps about you and why. We try to collect as little as possible.
  </p>

  <h2 class="text-lg font-bold mt-6 mb-2">Who's responsible</h2>
  <p class="mb-4">
    The controller of your data is <b>FatCloud</b> (KvK 42069102), Jacques Veltmanstraat 54, Amsterdam,
    Netherlands — contact <a href="mailto:info@fatcloud.nl">info@fatcloud.nl</a>. We process your data
    to run your account and the service (to perform our agreement with you), to send the optional
    monthly summary (only with your consent), and to power the city directory (our legitimate interest,
    which you can object to at any time — see below). Your passcode is only ever stored as a salted hash.
  </p>

  <h2 class="text-lg font-bold mt-6 mb-2">What we store</h2>
  <ul class="list-disc pl-5 space-y-1 mb-4">
    <li><b>Your account:</b> your name/handle, and — if you sign in with Google or Apple — the ID and profile photo they share with us. You can connect more than one sign-in method (a passcode plus Google and/or Apple) so you don't lose access; you can disconnect the extra ones any time.</li>
    <li><b>Email (optional):</b> if you sign in with Google/Apple we receive your email; with a passcode account it's optional. We store it to identify your account and, <b>only if you opt in</b>, to send your monthly summary (see below).</li>
    <li><b>Optional answers:</b> if you choose to, an age range, where you're from, and how you heard about us. You can skip these.</li>
    <li><b>Your games:</b> the buy-ins, results and settlements you enter, and reactions/comments you post.</li>
    <li><b>A login cookie:</b> a single signed cookie that keeps you logged in. No third-party tracking or advertising cookies.</li>
  </ul>

  <h2 class="text-lg font-bold mt-6 mb-2">Why we use it</h2>
  <ul class="list-disc pl-5 space-y-1 mb-4">
    <li>To run the app, keep you signed in, and show your stats and history.</li>
    <li>To contact you about your account if needed.</li>
    <li><b>Monthly summary email:</b> <b>only if you opt in</b> (an unticked checkbox when you sign up, or the toggle in your <a href="/account">account settings</a>) we send a once-a-month recap of <i>your own</i> stats — your best and worst night, average profit and a few highlights from the last 30 days. Every email also has a one-tap <b>unsubscribe</b> link, and you can turn it off any time. We don't send any other marketing email, and we never share your address.</li>
    <li>The optional answers help us understand who uses potcount. They're never required.</li>
  </ul>

  <h2 class="text-lg font-bold mt-6 mb-2">Public profile &amp; the city directory</h2>
  <p class="mb-4">
    potcount has a public directory that helps people find local home games by city (for example
    <span class="font-mono">/homegames/amsterdam</span>). These pages are public and can be indexed
    by search engines. What appears there depends on your <b>profile visibility</b> setting:
  </p>
  <ul class="list-disc pl-5 space-y-1 mb-4">
    <li>If your profile is <b>public</b>, your handle, display name, profile photo and city may be
    shown in your city's directory and used in city rankings.</li>
    <li>If your profile is <b>members-only</b> or <b>private</b>, you are <b>not</b> listed in the
    directory, and your detailed stats stay hidden as usual.</li>
    <li>If you host or join a public "open game", the game (and that you're seated at it) is visible
    on its city page. Open games are shown in blinds only — never a real-money amount.</li>
    <li>Anyone who has told us they are <b>under 18</b> is excluded from the public directory entirely.</li>
  </ul>
  <p class="mb-4">
    To keep this low-impact, the directory shows <b>only your username and city</b> — never your
    real name or profile photo. Those appear only on your profile page itself, and only if your
    profile is public.
  </p>
  <p class="mb-4">
    <b>Our legal basis for this listing is our legitimate interest</b> (Article 6(1)(f) GDPR) in
    helping people find local home games, balanced against the limited, pseudonymous information
    shown. <b>You have the right to object at any time</b> (Article 21 GDPR): set your profile to
    members-only or private in your <a href="/account">account settings</a> to remove yourself from
    the directory, or clear your city so you're no longer grouped under one. We act on this
    immediately, no questions asked. (Search engines may take a little while to drop a page they've
    already cached.)
  </p>

  <h2 class="text-lg font-bold mt-6 mb-2">Where it's kept</h2>
  <p class="mb-4">
    Your data lives on our own server hosted in the EU (Frankfurt), with encrypted daily backups
    stored via Cloudflare R2. We do <b>not</b> sell your data or share it with advertisers. Sign-in
    is handled by Google and Apple under their own privacy policies; we only receive the basic
    profile details described above.
  </p>
  <p class="mb-4">
    We share data only with the providers that help us run potcount, under data-processing agreements:
    <b>Google</b> and <b>Apple</b> (only if you choose to sign in with them), <b>Resend</b> (to deliver
    account and monthly-summary emails, if you have an email on file), and <b>Cloudflare R2</b>
    (encrypted backups). Some of these providers are US-based, so limited data (email delivery, sign-in)
    may be transferred outside the EEA; where that happens it is protected by the <b>EU–US Data Privacy
    Framework and/or Standard Contractual Clauses</b>. We never sell or rent your data or share it with
    advertisers.
  </p>

  <h2 class="text-lg font-bold mt-6 mb-2">Your choices and rights</h2>
  <ul class="list-disc pl-5 space-y-1 mb-4">
    <li><b>Newsletter:</b> opt in or out whenever you like.</li>
    <li><b>Email:</b> add, change or remove the optional email on your account at any time.</li>
    <li><b>Access &amp; correction:</b> you can edit your profile yourself, or ask us for a copy of your data.</li>
    <li><b>Deletion:</b> you can delete your account yourself — see below — or ask us to do it for you.</li>
    <li><b>Portability, restriction &amp; objection:</b> ask for your data in a portable form, ask us to restrict processing, or object to processing based on our legitimate interest (including the city directory).</li>
  </ul>
  <p class="mb-4">
    To exercise any of these, email <a href="mailto:info@fatcloud.nl">info@fatcloud.nl</a> — we respond
    within one month. You also have the right to complain to the Dutch data-protection authority, the
    <b>Autoriteit Persoonsgegevens</b>
    (<a href="https://autoriteitpersoonsgegevens.nl" target="_blank" rel="noopener">autoriteitpersoonsgegevens.nl</a>).
  </p>

  <h2 class="text-lg font-bold mt-6 mb-2">Deleting your account</h2>
  <p class="mb-4">
    On your <a href="/account">account page</a> there's a <b>Delete account</b> option. To prevent
    accidents it asks you to type your username (and your passcode, if you use one) to confirm. When
    you delete:
  </p>
  <ul class="list-disc pl-5 space-y-1 mb-4">
    <li>Your account record — name/handle, email, passcode, any linked Google/Apple sign-ins, optional answers and follows — is <b>erased</b>, and your username is freed up.</li>
    <li>Comments and reactions you posted are removed.</li>
    <li>Games you played in <b>stay</b>, because they're shared with the other players — but your seat is <b>unlinked</b> from you: it keeps the buy-ins and result under the name on the table, with no link back to a profile. Nobody can reach your (now deleted) profile from those games.</li>
  </ul>
  <p class="mb-4">
    Deletion is immediate and can't be undone. For a short period afterwards a copy may still exist
    in our encrypted daily backups; those rotate automatically and any deleted data is gone from them
    within about two weeks.
  </p>
  <p class="mb-4">
    For any of the above, or any privacy question, email
    <a href="mailto:info@fatcloud.nl">info@fatcloud.nl</a>.
  </p>

  <h2 class="text-lg font-bold mt-6 mb-2">Cookies</h2>
  <p class="mb-4">
    potcount uses a <b>single, strictly-necessary session cookie</b> to keep you logged in. Under the
    Dutch Telecommunicatiewet this kind of cookie needs no consent, so we show no cookie banner. We set
    <b>no</b> advertising or third-party tracking cookies, and our usage analytics is first-party and
    cookieless.
  </p>

